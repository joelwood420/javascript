// Declare a variable n and assign it the value 5
let n = 5;

// Declare a variable sum and assign it 0 - this will store the running total
let sum = 0;

// Loop from 1 to n (inclusive)
for (let i = 1; i <= n; i++) {
  // Add the current value of i to the running total sum
  sum += i;
}

// Output the final sum to the console
console.log(sum);

// Summary of code actions:
// - Declared a variable n to set the upper limit of the loop
// - Declared a variable sum to store the cumulative total
// - Used a for loop to iterate from 1 up to and including n
//   - In each iteration, the value of i is added to sum
// - After the loop, the total sum of numbers from 1 to n is printed to the console
